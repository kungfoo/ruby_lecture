a = [3,4,3,2,1,4,5,6]

a.delete_if{ |i| i%2 == 0 } # => [3, 3, 1, 5]

[1,2,3,4].collect{ |x| x+2 } # => [3, 4, 5, 6]

b = []
b.fill(0..9){ |i| i*i } # => [0, 1, 4, 9, 16, 25, 36, 49, 64, 81]
b.select { |x| x%4 == 0 } - [0] # => [4, 16, 36, 64]

b.sort{ |i,j| j <=> i } # => [81, 64, 49, 36, 25, 16, 9, 4, 1, 0]

[1,2].each_index{ |i| puts "#{i}: #{a[i]}" }
# >> 0: 3
# >> 1: 3
