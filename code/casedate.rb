require "date"

start = Date.new(2009, 2, 20)
stop = Date.new(2009, 3, 12)

days = (start..stop)

puts days.to_a.join(', ')
# >> 2009-02-20, 2009-02-21, 2009-02-22, 2009-02-23, 2009-02-24,
# >> 2009-02-25, 2009-02-26, 2009-02-27, 2009-02-28, 2009-03-01,
# >> 2009-03-02, 2009-03-03, 2009-03-04, 2009-03-05, 2009-03-06,
# >> 2009-03-07, 2009-03-08, 2009-03-09, 2009-03-10, 2009-03-11,
# >> 2009-03-12

date = Date.new(2009, 7, 2)
puts days === date # => false